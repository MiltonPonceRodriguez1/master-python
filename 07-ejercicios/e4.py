"""

Ejercicio No.4

Pedir dos numeros al usuario y hacer todas las
operaciones básicas de una calculadora por pantalla

"""

a = int(input("Introduzca el número A: "))
b = int(input("Introduzca el número B: "))
print("\n\t\t\t########## Calculadora ##########")
print(f"	  Suma  	-> {a} + {b} = {a+b}")
print(f"	  Resta 	-> {a} - {b} = {a-b}")
print(f"   Multiplicacion  -> {a} * {b} = {a*b}")
print(f"	 Division 	-> {a} / {b} = {a/b}")
print(f"	  Modulo	-> {a} % {b} = {a%b}")