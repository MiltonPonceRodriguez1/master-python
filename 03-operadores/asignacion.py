# Operadores de asignación
edad = 55
print(edad)

# Operador de asignacion con suma
edad += 5
print(edad)

# Operador de asignacion con resta
edad -= 5
print(edad)

# Operador de asignacion con multiplicación
edad *= 2
print(edad)

# Operador de asignacion con division
edad /= 2
print(edad)

# Operadores de incremento y decremento
year = 2021

# Incremento
# year = year + 1
year += 1

# Decremento
# year = year - 1
year -= 1

# Pre Incremento
year = 1 + year

# Pre Decremento
year = 1 - year

print(year)